/* demonstrating block of code */

class BlockTest{
    public static void main(String args[]){
        int x,y;

        y = 20;

        for(x=0;x<=10;x++){
            System.out.println("This is value of x: "+x);
            System.out.println("This is the value of y: "+y);
            y = y - 2;
        }
    }
}